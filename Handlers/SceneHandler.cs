        public Dictionary<Point, string> GeneratePicture(string[,] schematic)
        {
            
                int x2 = (((90 - schematic.GetLength(1)) / 2) + schematic.GetLength(1));
                Dictionary<Point, string> dictionarySchematic = new Dictionary<Point, string>();
                for (int y = 0; y < schematic.GetLength(0); y++)
                {
                    for (int x = ((90 - schematic.GetLength(1)) / 2); x < x2; x++)
                    {
                        Point point = new Point(x, y);
                        dictionarySchematic.Add(point, schematic[y, (x - ((90 - schematic.GetLength(1)) / 2))]);
                    }
                }
                return dictionarySchematic;
            }   
            

