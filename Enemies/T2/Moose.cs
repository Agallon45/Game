        public override string SpecialAtk(Player player)
        {
            double dmg = CurrentHealthPoints;
            player.CurrentHealthPoints -= dmg;
            double dmg2 = Math.Round(Strength / 2);
            CurrentHealthPoints -= dmg2;
            return $"{name} tries to gore {player.name}. {player.name} suffers {dmg} damage, while {name} suffers {dmg2}";
        }

        public override string RemoveBuffs()
        {
            if (debuffed)
            {
                if (roundNum == roundNumRemove)
                {
                    Strength += negStrength;
                    negStrength = 0;
                    Agility += negAgility;
                    negAgility = 0;
                    debuffed = false;
                    roundNumRemove = 0;
                    return $"{name} is no longer debuffed";
                }
            }
            if (isDotted)
            {
                if (roundNum == roundNumRemove)
                {
                    dot = 0;
                    isDotted = false;
                    return $"{name} is no longer suffering periodic damage.";
                }
                else
                {
                    CurrentHealthPoints -= dot;
                    return $"{name} suffers {dot} damage!";
                }
            }
            else
            {
                return "";
            }
        }
